package com.javarush.task.pro.task04.task0405;

/* 
Незаполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int high = 0;
        while (high < 10){
            int length = 0;
            System.out.println();
            high++;
            while (length < 20){
                System.out.print("Б");
                length++;
            }
        }

    }
}