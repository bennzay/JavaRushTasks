package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int stolb = 0;

        while (stolb < 5) {
            int str = 0;
            while (str < 10) {
                System.out.print("Q");
                str++;
            }
            System.out.println();
            stolb++;
        }

    }
}